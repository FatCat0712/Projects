    private void jButton_UpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_UpdateActionPerformed
        // TODO add your handling code here:
        studentList = Manager.getStudentList();
        int id = Integer.parseInt(jText_Id.getText().trim());
        String name = jText_Name.getText();
        String cityName = jText_BirthPlace.getText().trim();
        String[] cities = Manager.setCity();
        int cityIndex = 0;
        for(int i = 1; i < cities.length; i++){
            if(cities[i].equals(cityName)){
                cityIndex = i;
                break;
                
            }
        }
        City city = new City(cityIndex,cityName);
        String dob = jText_DateOfBirth.getText();
        String sex;
        if(jRadioButton_Male.isSelected()){
            sex = "Male";
        }
        else{
            sex = "Female";
        }
        float subject1 = Float.parseFloat(jText_Subject1.getText());
        float subject2 = Float.parseFloat(jText_Subject2.getText());
        float subject3 = Float.parseFloat(jText_Subject3.getText());
        String photo = Manager.encode(pictureFile);
        
        student = new Student(id, name, city, dob, sex, subject1, subject2, subject3, photo);
        
        ArrayList<Student> students = studentList.displayStudentList();
        boolean updateStatus = false;
        for(Student s : students){
            if(s.getStudentId() == id){
                updateStatus = studentList.updateStudent(student);
                break;
            }
        }
        if(updateStatus){
            JOptionPane.showMessageDialog(this, "Update successfully");
        }
        else{
            JOptionPane.showMessageDialog(this, "Cannot update", "Error", JOptionPane.ERROR_MESSAGE);
        }
        this.dispose();
    }//GEN-LAST:event_jButton_UpdateActionPerformed

