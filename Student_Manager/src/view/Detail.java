    public static void showInfo(){
        student = Manager.getStudent();
        int studentId = student.getStudentId();
        String name = student.getStudentName();
        String city = student.getCity().getCityName();
        String dateOfBirth = student.getDateOfBirth();
        String sex = student.getSex();
        float subject1 = student.getSubject1();
        float subject2 = student.getSubject2();
        float subject3 = student.getSubject3();
        float total = subject1 + subject2 + subject3;
        Image photo = decode(student.getPhoto());
        
        jText_Id.setText(studentId+"");
        jText_Name.setText(name);
        jText_BirthPlace.setText(city);
        jText_DateOfBirth.setText(dateOfBirth);
        if(sex.equals("Male")){
            jRadioButton_Male.setSelected(true);
        }
        else{
            jRadioButton_Female.setSelected(true);
        }
        jText_Subject1.setText(subject1+"");
        jText_Subject2.setText(subject2+"");
        jText_Subject3.setText(subject3+"");
        jText_Total.setText(total+"");
        Image picture = photo.getScaledInstance(jLabel_Photo.getWidth(), jLabel_Photo.getHeight(), Image.SCALE_SMOOTH);
        ImageIcon icon = new ImageIcon(picture);
        jLabel_Photo.setIcon(icon);
        
    }
    public static Image decode(String encodedString){
        BufferedImage image = null;
        try {
            byte[] decodeString = Base64.getDecoder().decode(encodedString);
            ByteArrayInputStream inputStream = new ByteArrayInputStream(decodeString);
            image = ImageIO.read(inputStream);
            return image;
        } catch (IOException ex) {
            Logger.getLogger(Detail.class.getName()).log(Level.SEVERE,null, ex);
        }
        return image;
    }
    
