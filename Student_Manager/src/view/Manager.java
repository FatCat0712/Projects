    public static String[] setCity(){
        File cityFile = new File("E:\\Java\\Project\\Student_Manager\\src\\city.txt");
        String[] city = new String[64];
        
        try {
            FileInputStream fis = new FileInputStream(cityFile);
            InputStreamReader streamReader = new InputStreamReader(fis,"utf8");
            BufferedReader br = new BufferedReader(streamReader);
            String lines;
            
            int i = 1;
            while((lines = br.readLine()) != null){
                lines = lines.substring(2).trim();
                city[i] = lines;
                i++;
            }
        } catch (FileNotFoundException ex) {
            Logger.getLogger(test.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(test.class.getName()).log(Level.SEVERE, null, ex);
        }
        DefaultComboBoxModel dm = new DefaultComboBoxModel<>(city);
        jComboBox_City.setModel(dm);
        return city;
        
        
    }
    
   
    
    
