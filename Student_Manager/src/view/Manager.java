    private void jButton_SaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_SaveActionPerformed
        // TODO add your handling code here:
        JFileChooser fileChooser = new JFileChooser("E:\\Java\\Project\\Student_Manager\\src");
        fileChooser.setFileSelectionMode(JFileChooser.FILES_AND_DIRECTORIES);
        int returnValue = fileChooser.showSaveDialog(this);
        if(returnValue == JFileChooser.APPROVE_OPTION){
            String path = fileChooser.getSelectedFile().getAbsolutePath();
            File saveFile = new File(path);
            try {
                if(!saveFile.exists()){
                    saveFile.createNewFile();
                }
                FileOutputStream fos = new FileOutputStream(saveFile,false);
                ObjectOutputStream oos = new ObjectOutputStream(fos);
                ArrayList<Student> students = studentList.displayStudentList();
                for(Student s: students){
                   oos.writeObject(s);
                   oos.flush();
                }
                JOptionPane.showMessageDialog(this, "Saved successfully");
                
            } catch (IOException e) {
                e.printStackTrace();
                JOptionPane.showMessageDialog(this, "Cannot save");
                
            }
           
            
            
            
        }
    }//GEN-LAST:event_jButton_SaveActionPerformed

    private void jButton_LoadActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_LoadActionPerformed
        // TODO add your handling code here:
        try {
           
            studentList = new StudentList();
            JFileChooser fileChooser = new JFileChooser("E:\\Java\\Project\\Student_Manager\\src");
            fileChooser.setFileSelectionMode(JFileChooser.FILES_AND_DIRECTORIES);
            int returnValue = fileChooser.showOpenDialog(this);
            if(returnValue == JFileChooser.APPROVE_OPTION){
                String path = fileChooser.getSelectedFile().getAbsolutePath();
                File loadFile = new File(path);
                FileInputStream fis = new FileInputStream(loadFile);
                ObjectInputStream ois = new ObjectInputStream(fis);
                while(fis.available() > 0){
                    Student s = (Student) ois.readObject();
                    studentList.addStudent(s);
                }
                tblModel = (DefaultTableModel) jTable_Student.getModel();
                tblModel.setRowCount(0);

                ArrayList<Student> students = studentList.displayStudentList();
                for(Student s : students){
                    int studentId = s.getStudentId();
                    String studentName = s.getStudentName();
                    String c = s.getCity().getCityName();
                    String d = s.getDateOfBirth();

                    String m = s.getSex();
                    float s1 = s.getSubject1();
                    float s2 = s.getSubject2();
                    float s3 = s.getSubject3();
                    String photo = s.getPhoto();

                    String[] data = {studentId+"",studentName,c,d,m,s1+"",s2+"",s3+"",photo};
                    tblModel = (DefaultTableModel) jTable_Student.getModel();
                    tblModel.addRow(data);
                }
            
            }
        } catch (HeadlessException | FileNotFoundException e) {
        } catch (IOException | ClassNotFoundException ex) {
            Logger.getLogger(Manager.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_jButton_LoadActionPerformed

