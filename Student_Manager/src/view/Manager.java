    public static String encode(File picture){
        String base64 = "";
        try {
            if(picture != null){
                 byte[] decodedByte = Files.readAllBytes(Path.of(picture.getAbsolutePath()));
                base64 = Base64.getEncoder().encodeToString(decodedByte);
            }
           
        } catch (IOException ex) {
            Logger.getLogger(Manager.class.getName()).log(Level.SEVERE, null, ex);
        }
        return base64;
    }
    
    
    
