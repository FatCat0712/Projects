    private void jTable_StudentMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable_StudentMouseClicked
        // TODO add your handling code here:
            int selectedRow = jTable_Student.getSelectedRow();
            String studentId = (String) jTable_Student.getModel().getValueAt(selectedRow,0);
            String name = (String) jTable_Student.getModel().getValueAt(selectedRow,1);
            
            String birthPlace = (String) jTable_Student.getModel().getValueAt(selectedRow,2);
            String[] cities = setCity();
            int cityIndex = 0;
            for(int i = 1; i < cities.length; i++){
                if(cities[i].equals(birthPlace)){
                    cityIndex = i;
                    break;
                }
            }
            City city = new City(cityIndex, birthPlace);
            String dateOfBirth = (String)jTable_Student.getModel().getValueAt(selectedRow,3);
            String sex = (String)jTable_Student.getModel().getValueAt(selectedRow,4);
            String subject1 = (String)jTable_Student.getModel().getValueAt(selectedRow,5);
            String subject2 = (String)jTable_Student.getModel().getValueAt(selectedRow,6);
            String subject3 = (String)jTable_Student.getModel().getValueAt(selectedRow,7);
            String photo = (String)jTable_Student.getModel().getValueAt(selectedRow, 8);
            
            
            student = new Student(Integer.parseInt(studentId), name, city, dateOfBirth, sex, Float.parseFloat(subject1),  Float.parseFloat(subject2),  Float.parseFloat(subject3),photo);
            
            jText_StudentId.setText(studentId);
            jText_Name.setText(name);
            jText_Birthplace.setText(birthPlace);
            jText_dateOfBirth.setText(dateOfBirth);
            if(sex.equals("Male")){
                jRadioButton_Male.setSelected(true);
            }
            else{
                jRadioButton_Female.setSelected(true);
            }
            jText_Subject1.setText(subject1);
            jText_Subject2.setText(subject2);
            jText_Subject3.setText(subject3);
    }//GEN-LAST:event_jTable_StudentMouseClicked

